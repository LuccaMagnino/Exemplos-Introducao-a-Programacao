#include <stdio.h>

int main() {
    double A[2][2];
    int i, j;  

  
    for (i = 0; i < 2; i++) {
        for (j = 0; j < 2; j++) {
            scanf("%lf", &A[i][j]);
        }
    }

    double A2[2][2];
    A2[0][0] = A[0][0] * A[0][0] + A[0][1] * A[1][0];
    A2[0][1] = A[0][0] * A[0][1] + A[0][1] * A[1][1];
    A2[1][0] = A[1][0] * A[0][0] + A[1][1] * A[1][0];
    A2[1][1] = A[1][0] * A[0][1] + A[1][1] * A[1][1];


    printf("%.3lf %.3lf\n", A2[0][0], A2[0][1]);
    printf("%.3lf %.3lf\n", A2[1][0], A2[1][1]);

    return 0;
}
